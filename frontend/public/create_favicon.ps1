# Create a simple 16x16 ICO file with orange background and white 'H'
$icoBytes = @(
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x10, 0x10, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x68, 0x04,
    0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x20, 0x00,
    0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
)

# Fill with orange background (simplified)
for ($i = 62; $i -lt 1126; $i += 4) {
    $icoBytes[$i] = 0x35     # Blue component
    $icoBytes[$i+1] = 0x6B   # Green component
    $icoBytes[$i+2] = 0xFF   # Red component  
    $icoBytes[$i+3] = 0xFF   # Alpha
}

[System.IO.File]::WriteAllBytes("favicon.ico", $icoBytes)
Write-Host "Created simple orange favicon.ico"
